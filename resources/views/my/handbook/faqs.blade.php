			<p><strong>Q. What is an Assessment Fee?</strong></p>
			<p><strong>A.</strong> This is a fee charged by Visa/MasterCard on each transaction worldwide at .11%. Its purpose is to cover the risk of fraud and loss due to Credit Card fraud and identity fraud that they incur each year.</p>
