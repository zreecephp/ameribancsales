			<p><strong>Q. What is a Discount Rate?</strong></p>
			<p><strong>A. </strong>The Discount Rate is a percentage of the electronic transaction each merchant pays to the processing bank. AmeriBanc&#8217;s discount rate for a swiped credit card and debit card (retail) transaction is 0.69% and 1.69% on Credit Cards. We also have a &#8220;Blended&#8221; rate of 1.29% for both credit and debit cards if the merchant needs. This would equal $1.29 on a $100 transaction, which is much lower than the industry standard of 1.6835%</p>
