			<p><strong>Q. What are Partially (Mid) Qualified and Non-Qualified Surcharges?</strong></p>
			<p><strong>A.</strong> These are added onto the qualified discount rate based on a specific type of card being taken. Many cards have Rewards attached to them, so Visa/MasterCard have increased fees for these transactions to be taken, and they are called Partially qualified, or also referred to as Mid-Qualified. These cards are built to incent the user to use it more often, and for increased amounts which is how the merchant benefits. Non-Qualified cards can be Government issued cards or Corporate level cards. The increased average ticket on these cards is obviously  based on the use.</p>
