			<i class="icons-home icon-ok" style="color:rgb(172,23,28)"></i> Verify the telephone number and write it on the check if it is not printed.<br />
			<i class="icons-home icon-ok" style="color:rgb(172,23,28)"></i> Verify the address on the face of the check.<br />
			<i class="icons-home icon-ok" style="color:rgb(172,23,28)"></i> The check is made out to the business name.<br />
			<i class="icons-home icon-ok" style="color:rgb(172,23,28)"></i> The bearer of the check must be the writer of the check and the owner of the account.<br />
			<br />
