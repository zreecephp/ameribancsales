			<p><strong>A.</strong> The Transaction/Communication fee is the cost of processing money through the worldwide Visa/MasterCard/Discover system. ABN&#8217;s Communication fee is 19 cents per transaction. Many times you see these referred to on other statements as WATS fees, Authorization fees, Per Item fees, and others, but all refer to the same cost.</p>
