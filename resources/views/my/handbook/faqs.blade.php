			<p><strong>Q. What is the $25 Monthly Minimum</strong></p>
			<p><strong>A.</strong> The $25 Monthly Minimum is the minimum cost to the merchant for the processing of the transactions they take during any given month. Easily explained: as a processor, we have a hard cost for a processing account of $25 for any given merchant, before we realize any profit. If a merchant would process $2000 at 1.19%, their cost would be $23.80. AmeriBanc would add $1.20 to their bill because it was under the &#8216;Monthly Minimum&#8217;. It is not an additional $25, but a break even line for us that once a merchant is beyond, becomes irrelevant, which is true for 98% of all merchants.</p>
